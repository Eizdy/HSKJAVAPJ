CREATE TABLE TaiKhoan (
    tenTK NVARCHAR(50) PRIMARY KEY,
    matKhau NVARCHAR(50)
);

CREATE TABLE NhanVien (
    maNV NVARCHAR(10) PRIMARY KEY,
    tenNV NVARCHAR(100),
    ngaySinh DATE,
    dienThoai NVARCHAR(20),
    email NVARCHAR(100),
    chucVu NVARCHAR(50),
